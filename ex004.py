V=(input('Digite algo '))
print('O tipo primitivo desse valor é:', type(V))
print('É alfabético?', V.isalpha())
print('É numérico?', V.isnumeric())
print('É alfanumérico?', V.isalnum())
print('Está em maiúsculas? {}!'.format(V.isupper()))
print('Está em minúsculas?', V.islower())
print('Está capitalizado?', V.isascii())
print('Só tem espaços?',V.isdigit())


